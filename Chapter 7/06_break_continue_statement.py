#Break statement:
for i in range(100):
    if(i==45):
        break   #Exit the iteration right now!
    print(i)

#Continue statement:
for  i in range(100):
    if(i==45):
        continue  #Skip the iteration
    print(i)

#Pass statement: it is a null statement in python
for i in range(100):
    pass #it instruct to do nothing.
 